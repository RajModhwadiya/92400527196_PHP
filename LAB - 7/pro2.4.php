<?php
//2.4 Write a that demonstrate the use of following string functions:
    $name="Raj Modhwadiya";

    //1) strlen() // any string
    echo strlen($name);
    echo "<br></br>";

    //2) strpos() //find some specific word,letter
    echo strpos($name,"M",0);
    echo "<br></br>";

    //3) str_word_count()
    echo str_word_count($name);
    echo "<br></br>";

    //4) strrev()
    echo strrev($name);
    echo "<br></br>";

    //5) strtolower()
    echo strtolower($name);
    echo "<br></br>";

    //6) strtoupper()
    echo strtoupper($name);

?>


